package by.htp.ll.bean;

public class User {
	private String name;
	private int yearBirthday;
	
	public User() {}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getYearBirthday() {
		return yearBirthday;
	}
	
	public void setYearBirthday(int yearBirthday) {
		this.yearBirthday = yearBirthday;
	}
}
